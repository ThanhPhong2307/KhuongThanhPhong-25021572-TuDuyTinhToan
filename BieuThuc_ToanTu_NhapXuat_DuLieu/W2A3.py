a, b = map(int, input().split())
tong = a + b
hieu = a - b
tich = a * b
phan_nguyen = a // b
phan_du = a % b
chia_thuc = a / b
print(f"Tong: {tong}")
print(f"Hieu: {hieu}")
print(f"Tich: {tich}")
print(f"Phan nguyen: {phan_nguyen}")
print(f"Phan du: {phan_du}")
print(f"Ket qua chia thuc: {chia_thuc}")